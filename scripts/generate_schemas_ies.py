#!/usr/bin/env python3
"""Generate the developer-facing ``schemas-ies/`` section — a schema.beckn.io-style
catalog and one term page per schema family.

For each family ``schemas/<Family>/`` this produces ``schemas-ies/<Family>.md``:

  * title + tagline + at-a-glance (canonical base URL, latest version, status)
  * a **Developer resources** table for the current version — the canonical
    ``https://india-energy-stack.github.io/ies-accelerator/schemas/<Family>/<ver>/…``
    URLs that resolve at runtime (schemas/ is served verbatim by GitHub Pages)
  * the current version's field reference, inline
  * every previous version below, each in a collapsible <details> with its own
    developer-resource table and field tables

and ``schemas-ies/README.md`` — the searchable catalog listing every family,
grouped by category, each linking to its page.

The field tables are built from the compiled ``schema.json`` in each version dir,
reusing the primitives in ``generate_field_tables.py`` so the two stay consistent.
Family-level metadata (category, use-case links) that is not in schema.json lives
in FAMILIES below; the tagline and status line are parsed from each family README.

Nothing here moves or copies the raw schema files — this is a presentation layer
that links to the canonical files under ``schemas/``.

Usage:
  python3 scripts/generate_schemas_ies.py            # regenerate the whole section
  python3 scripts/generate_schemas_ies.py --check    # CI: fail if anything is stale
"""
